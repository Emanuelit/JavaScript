//em JS temos let, const e var

let primeiroNome = "Felipe";
console.log(primeiroNome);

//acima, temos uma variavel declarada com let

let nomeDoMeio = "Miranda";
nomeDoMeio = "Marreiros";
console.log(nomeDoMeio);

//também podemos mudar uma varial quando acharmos necessário

const nomeCompleto = "Felipe Miranda Marreiros";
console.log(nomeCompleto);

//usando const para declarar uma variavel, temos em mente que o valor dela não irá mudar. Será estavel até o fim do programa

//Em const também não podemos mudar seu valor, nem deixa-lo vazio

var idade_um = 55;

//var é a ultima opção para declarar uma variavel, já que ela deve ser evitada no dia-a-dia